import json

BASE = "/var/repo/travel-planner/data"

# ─── 底層工廠（不做任何假設）───────────────────────────────
def make_record(id, name, name_en, category, zone, station_text,
                ticket, need_reserve, stay, cash_only,
                desc, tags, priority, sid):
    return {
        "id": id, "name": name, "name_en": name_en,
        "category": category, "sub_category": category,
        "parent": None,
        "location": zone, "station": station_text,
        "ticket": ticket, "need_reservation": need_reserve,
        "stay_duration": stay, "cash_only": cash_only,
        "description": desc, "sources": [],
        "tags": tags, "priority": priority,
        "zone": zone, "station_id": sid,
        "nearby_stations": [sid],
        "details": {
            "blog_article": [],
            "google_maps": f"https://www.google.com/maps/search/?api=1&query={name}+{zone}",
            "youtube": []
        }
    }

# ─── 通用寫入 ───────────────────────────────────────────────
def save(region, att_list):
    out = {"region": region, "updated": "2026-06-29", "attractions": att_list}
    path = f"{BASE}/{region}/attractions.json"
    with open(path, "w", encoding="utf-8") as f:
        json.dump(out, f, ensure_ascii=False, indent=2)
    cats = {}
    for a in att_list:
        c = a["category"]
        cats[c] = cats.get(c, 0) + 1
    print(f"✅ {region}: {len(att_list)} | " + " ".join(f"{k}={v}" for k, v in cats.items()))

# ─── 各地工廠 ───────────────────────────────────────────────
def factory(region):
    stations = json.load(open(f"{BASE}/{region}/stations.json"))["stations"]
    sid = {s["name"]: s["id"] for s in stations}
    S = lambda n: sid.get(n, stations[0]["id"])
    recs = []

    # 所有記錄型函式統一簽名：(id, name, name_en, category, zone, st_text,
    #                           ticket, need_reserve, stay, cash_only,
    #                           desc, tags, priority)
    def R(id, name, name_en, cat, zone, st_text, ticket, res, dur, cash, desc, tags, pri):
        recs.append(make_record(id, name, name_en, cat, zone, st_text,
                                ticket, res, dur, cash, desc, tags, pri, S(zone)))

    # A = attraction (多 need_reserve)
    def A(id, name, name_en, zone, st_text, ticket, res, dur, cash, desc, tags, pri):
        R(id, name, name_en, "attraction", zone, st_text, ticket, res, dur, cash, desc, tags, pri)

    # F = food
    def F(id, name, name_en, zone, st_text, ticket, dur, desc, tags, pri):
        R(id, name, name_en, "food", zone, st_text, ticket, False, dur, False, desc, tags, pri)

    # H = hidden_gem
    def H(id, name, name_en, zone, st_text, dur, desc, tags, pri):
        R(id, name, name_en, "hidden_gem", zone, st_text, "免費", False, dur, False, desc, tags, pri)

    # Sh = shrine
    def Sh(id, name, name_en, zone, st_text, dur, desc, tags, pri):
        R(id, name, name_en, "shrine", zone, st_text, "免費", False, dur, False, desc, tags, pri)

    return region, recs, save, A, F, H, Sh

